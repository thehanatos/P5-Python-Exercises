def sum(a, b):
    """
    Calculate the sum of two numbers

    Args:
        a (int or float): first number
        b (int or float): second number

    Returns:
        int or float: result of the sum a + b
    """
    return a + b


def subtraction(a, b):
    """
    Calculate the substraction of two numbers

    Args:
        a (int or float): first number
        b (int or float): second number

    Returns:
        int or float: result of the substraction a - b
    """
    return a - b
